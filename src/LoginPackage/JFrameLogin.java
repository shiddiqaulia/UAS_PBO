    private void handleLoginSuccess(String jabatan, String nama) {
        if (jabatan.equals("Manager")) {
            JOptionPane.showMessageDialog(this, "Login Succesfully, Welcome Manager !");
            JFrameHome home = new JFrameHome(nama, jabatan);
            home.show();
            this.dispose();
        } else if (jabatan.equals("Employee")) {
            JOptionPane.showMessageDialog(this, "Login Succesfully, Welcome Employee !");
            JFrameHomeEmployee homeEmployee = new JFrameHomeEmployee(nama, jabatan);
            homeEmployee.show();
            this.dispose();
        }
    }
