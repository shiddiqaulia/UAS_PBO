    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        Connection c = null;
//        Ini merupakan variable penangkap password char
        char[] passwordCharArray = txtPass.getPassword();
        String password = new String(passwordCharArray);
        
        try {
            c = Koneksi.getConnection();
            String sql = "SELECT * FROM user WHERE nip = ? AND password = ?";

            try (PreparedStatement ps = c.prepareStatement(sql)) {
                ps.setString(1, txtUser.getText());
                ps.setString(2, password);

                try (ResultSet r = ps.executeQuery()) {
                    if (r.next()){
                        String jabatan = r.getString("jabatan");
                        String nama = r.getString("nama");
                        handleLoginSuccess(jabatan, nama);

                    }else{
                        JOptionPane.showMessageDialog(null, "Wrong Username or Password");
                        txtPass.requestFocus();
                    }
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        } finally {
            // Tutup koneksi setelah selesai
            try {
                if (c != null && !c.isClosed()) {
                    c.close();
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

